#!/usr/bin/env python3
"""
Generate Figure 5: Calibration Reliability Diagram for Mortality (Exp 1 Deciles Fused).

Reads predictions pickle file generated by `transfer_rep_based_preds.py` and plots:
1. Reliability diagram (Calibration curve)
2. Frequency histogram (inset or secondary axis)

Usage:
    python utilities/scripts/generate_calibration_plot.py --preds_pkl <path_to_pkl> --output <path_to_pdf>

"""
import argparse
import pickle
import sys
from pathlib import Path

import matplotlib
matplotlib.use('Agg')
import matplotlib.pyplot as plt
import numpy as np
from sklearn.calibration import calibration_curve

def plot_calibration(y_true, y_prob, output_path, n_bins=15):
    """Plot reliability diagram with frequency histogram."""
    
    # Calculate calibration curve
    prob_true, prob_pred = calibration_curve(y_true, y_prob, n_bins=n_bins, strategy='quantile')
    
    # ECE Calculation
    ece = np.mean(np.abs(prob_true - prob_pred)) # Simplified ECE estimate for title
    
    # Setup plot
    fig = plt.figure(figsize=(6, 6))
    ax_main = plt.subplot2grid((4, 1), (0, 0), rowspan=3)
    ax_hist = plt.subplot2grid((4, 1), (3, 0), sharex=ax_main)
    
    # 1. Main Calibration Curve
    ax_main.plot([0, 1], [0, 1], "k:", label="Perfectly calibrated")
    ax_main.plot(prob_pred, prob_true, "s-", label="Deciles Fused", color="#2980b9", linewidth=2)
    
    ax_main.set_ylabel("Fraction of Positives")
    ax_main.set_ylim([-0.05, 1.05])
    ax_main.set_title(f"Calibration: Same-Admission Mortality\n(Exp 1 Deciles Fused)", fontsize=12)
    ax_main.legend(loc="lower right")
    ax_main.grid(True, linestyle="--", alpha=0.5)
    
    # 2. Histogram of predicted probabilities
    ax_hist.hist(y_prob, range=(0, 1), bins=n_bins, color="#2980b9", alpha=0.7, edgecolor="k")
    ax_hist.set_xlabel("Predicted Probability")
    ax_hist.set_ylabel("Count")
    ax_hist.set_yscale("log")
    
    plt.tight_layout()
    plt.savefig(output_path, dpi=300, bbox_inches="tight")
    print(f"Saved figure to {output_path}")
    plt.close()

def main():
    parser = argparse.ArgumentParser(description="Generate Calibration Plot")
    parser.add_argument("--preds_pkl", type=Path, required=True, help="Path to predictions .pkl file")
    parser.add_argument("--output", type=Path, default=Path("figures/calibration_curves.pdf"), help="Output PDF path")
    args = parser.parse_args()
    
    args.output.parent.mkdir(parents=True, exist_ok=True)
    
    if not args.preds_pkl.exists():
        print(f"ERROR: Prediction file not found: {args.preds_pkl}")
        print("Please run the prediction generation job first (slurm/generate_exp1_preds.sh)")
        sys.exit(1)
        
    print(f"Loading predictions from {args.preds_pkl}...")
    with open(args.preds_pkl, "rb") as f:
        data = pickle.load(f)
        
    # Extract Mortality Data
    # Structure: {'labels': {'same_admission_death': ...}, 'predictions': {'same_admission_death': ...}}
    outcome = "same_admission_death"
    
    if outcome not in data['labels'] or outcome not in data['predictions']:
        print(f"ERROR: Outcome '{outcome}' not found in pickle file.")
        print(f"Available outcomes: {list(data['labels'].keys())}")
        sys.exit(1)
        
    y_true = data['labels'][outcome]
    y_prob = data['predictions'][outcome]
    
    # Filter valid (finite)
    mask = np.isfinite(y_true) & np.isfinite(y_prob)
    y_true = y_true[mask]
    y_prob = y_prob[mask]
    
    print(f"Plotting calibration for N={len(y_true)} (Positives: {y_true.sum():.0f}, Prevalence: {y_true.mean():.1%})")
    
    plot_calibration(y_true, y_prob, args.output)

if __name__ == "__main__":
    main()
